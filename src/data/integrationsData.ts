export type IntegrationStatus = 'Active' | 'Paused' | 'Error' | 'Not configured' | 'Deprecated';
export type IntegrationHealth = 'Healthy' | 'Degraded' | 'Down' | 'Unknown';
export type IntegrationEnvironment = 'Production' | 'Staging' | 'Sandbox';

export interface SparkPoint { t: number; v: number }

