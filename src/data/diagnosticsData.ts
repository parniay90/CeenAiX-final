export type Priority = 'stat' | 'urgent' | 'routine';
export type SampleStatus = 'critical' | 'resulted' | 'running' | 'received' | 'pending' | 'verified';
export type StudyStatus = 'scanning' | 'report_pending' | 'report_overdue' | 'scheduled' | 'complete' | 'released';
export type Modality = 'MRI' | 'CT' | 'USS' | 'XR' | 'PET' | 'MAMMO' | 'FLUORO';

export interface LabSample {
  id: string;
  sampleNum: string;
  patientName: string;
  patientId: string;
  patientAge: string;
  patientGender: string;
  priority: Priority;
  status: SampleStatus;
  tests: string[];
  criticalValues?: { test: string; value: string; unit: string; flag: string; refRange?: string }[];
  receivedTime: string;
  resultTime?: string;
  tat?: string;
  tatTarget?: string;
  tatOverdue?: boolean;
  notified?: boolean;
  doctor?: string;
  department?: string;
  sampleType?: string;
  location?: string;
  insurance?: string;
  queuePosition?: number;
  totalTests?: number;
  completedTests?: number;
  analyzer?: string;
}

